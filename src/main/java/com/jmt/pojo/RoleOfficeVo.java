package com.jmt.pojo;
/**
 * 角色机构的包装类
 * @author T430
 *
 */
public class RoleOfficeVo {
	private Role role;//角色
	private Office office;//办公机构
	private User user;//用户
	
	public Role getRole() {
		return role;
	}
	public void setRole(Role role) {
		this.role = role;
	}
	public Office getOffice() {
		return office;
	}
	public void setOffice(Office office) {
		this.office = office;
	}
	public User getUser() {
		return user;
	}
	public void setUser(User user) {
		this.user = user;
	}
	
	
	
}
